#!/usr/bin/env node
/**
 * generate-mdi-icons.js
 * Usage: node scripts/generate-mdi-icons.js > retro-panel/app/static/js/mdi-icons.js
 *
 * Requires @mdi/js installed in scripts/:
 *   cd scripts && npm install
 */
'use strict';

var mdi = require('./node_modules/@mdi/js');

function toKebab(s) {
  return s
    .replace(/^mdi/, '')
    .replace(/([A-Z])/g, function (m) { return '-' + m.toLowerCase(); })
    .replace(/^-/, '');
}

var entries = [];
Object.keys(mdi).sort().forEach(function (key) {
  if (key === 'default') { return; }         // skip ES module default export
  var name = toKebab(key);
  var path = mdi[key];
  if (typeof path !== 'string') { return; }  // skip non-path exports
  entries.push("    '" + name + "': '" + path + "'");
});

var output = [
  '/**',
  ' * mdi-icons.js — Full Material Design Icons for Retro Panel',
  ' * Generated by scripts/generate-mdi-icons.js from @mdi/js@' + require('./node_modules/@mdi/js/package.json').version,
  ' * Do not edit manually — regenerate with: cd scripts && npm install && node ../scripts/generate-mdi-icons.js > ../retro-panel/app/static/js/mdi-icons.js',
  ' * ES5-safe. No ES modules.',
  ' */',
  '(function () {',
  "  'use strict';",
  '  var P = {',
  entries.join(',\n'),
  '  };',
  "  var N = Object.keys(P).sort();",
  '  window.RP_MDI_PATHS = P;',
  '  window.RP_MDI_NAMES = N;',
  '  window.RP_MDI = function (name, size) {',
  '    var d = P[name];',
  "    if (!d) { return ''; }",
  "    return '<svg xmlns=\"http://www.w3.org/2000/svg\" width=\"' + size + '\" height=\"' + size",
  "      + '\" viewBox=\"0 0 24 24\" fill=\"currentColor\"><path d=\"' + d + '\"/></svg>';",
  '  };',
  '})();',
].join('\n');

process.stdout.write(output + '\n');
